const mongoose = require('mongoose');

const authoritySchema = new mongoose.Schema({
    employeeId: {
        type: String,
        required: true,
        unique: true
    },
    fullName: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

module.exports = mongoose.model('Authority', authoritySchema);
